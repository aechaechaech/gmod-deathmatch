local function spawn_ent_on_random_point(ent_class, max_ents)
    if #ents.FindByClass(ent_class) < max_ents then
        local ent_to_spawn = ents.Create( ent_class )
        randomPoint =  spawns[math.random(#spawns)]

        ent_to_spawn:SetPos(Vector(randomPoint[1], randomPoint[2], randomPoint[3]))
        ent_to_spawn:Spawn()
    end
end

timer.Create("powerupDropper", 60, 0, function()
    spawn_ent_on_random_point("powerup", 4)
end)

timer.Create("ammoDropper", 60, 0, function()
    spawn_ent_on_random_point("ammo", 6)
end)